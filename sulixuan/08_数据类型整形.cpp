//语法：数据类型 变量名 = 变量初始值

//int a = 10;

//数据类型存在意义
//给变量分配合适的内存空间

#include <iostream>
using namespace std;

int main(){
    // 整型
    //1.短整型
    short num1 = 10;
    //2.整型
    int num2 = 10;
    //3.长整型
    long num3 = 10;
    //4.长长整型
    long long  num4 = 10;
    
    cout <<"num1 = "<< num1 << endl;
    cout <<"num2 = "<< num2 << endl;
    cout <<"num3 = "<< num3 << endl;
    cout <<"num4 = "<< num4 << endl;

    cin.get(); 
    return 0;
}


