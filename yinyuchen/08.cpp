#include <iostream>
using namespace std;

int main()
{
    // 1.短整形
    short s = 10;
    // 2.整型
    int i = 20;
    // 3.长整形
    long l = 30;
    // 4.长长整形
    long long ll = 40;
    cout << "short s = " << s << endl;
    cout << "int i = " << i << endl;
    cout << "long l = " << l << endl;
    cout << "long long ll = " << ll << endl;
    system("pause");
    return 0;
}